// 从数据库读取，DB 优先；DB 不可用时回退到默认
function getWorldSummary() {
  try {
    const db = require('../db');
    const row = db.prepare("SELECT value FROM settings WHERE key = 'world_prompt'").get();
    return row && row.value ? row.value : DEFAULT_SUMMARY;
  } catch (e) {
    return DEFAULT_SUMMARY;
  }
}

